/**
 * Prepend the LITLABS_IDENTITY to a system prompt so the model always knows
 * which project it is inside. If a system prompt is already provided, the
 * identity goes first (highest priority), then the caller's system prompt.
 *
 * This is the helper used by the LLM layer (`llm.ts`) to guarantee the
 * identity is present on every call, regardless of what the caller passes.
 */
export function withLittIdentity(systemPrompt?: string): string {
  if (!systemPrompt) return LITLABS_IDENTITY;
  return `${LITLABS_IDENTITY}\n\n---\n\n${systemPrompt}`;
}

/**
 * Merge the static project identity with an OPTIONAL dynamic user project
 * context (from localStorage / Supabase). The identity is always present;
 * the user context is appended below it when supplied.
 *
 * Used by `buildSystemPrompt()` in `agents.ts` so agents always carry the
 * project identity even before the user fills in custom context.
 */
export function mergeLittIdentityWithProject(
  userContext?: { name?: string; description?: string; stack?: string; goals?: string; repoUrl?: string; customInstructions?: string },
): string {
  if (!userContext) return LITLABS_IDENTITY;
  const lines: string[] = [];
  if (userContext.name) lines.push(`Project name: ${userContext.name}`);
  if (userContext.description) lines.push(`Description: ${userContext.description}`);
  if (userContext.stack) lines.push(`Stack note: ${userContext.stack}`);
  if (userContext.goals) lines.push(`Current goals: ${userContext.goals}`);
  if (userContext.repoUrl) lines.push(`Repo: ${userContext.repoUrl}`);
  if (userContext.customInstructions) lines.push(`Custom instructions: ${userContext.customInstructions}`);
  if (!lines.length) return LITLABS_IDENTITY;
  return `${LITLABS_IDENTITY}\n\n---\n\nUSER-PROVIDED PROJECT NOTES (in addition to the identity above):\n${lines.join("\n")}\n`;
}

/**
 * `true` if the static identity block is in place. Used by health checks
 * to verify the LLM layer has the project brain wired up.
 */
export function littIdentityReady(): boolean {
  return LITLABS_IDENTITY.length > 200 && LITLABS_IDENTITY.includes("litlabs.net");
}

